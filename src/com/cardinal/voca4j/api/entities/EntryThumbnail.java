package com.cardinal.voca4j.api.entities;

public class EntryThumbnail {
	private String mime, urlOriginal, urlSmallThumb, urlThumb, urlTinyThumb;

	public String getMimeType() {
		return mime;
	}

	public String getUrlOriginal() {
		return urlOriginal;
	}

	public String getUrlSmallThumb() {
		return urlSmallThumb;
	}

	public String getUrlThumb() {
		return urlThumb;
	}

	public String getUrlTinyThumb() {
		return urlTinyThumb;
	}
}